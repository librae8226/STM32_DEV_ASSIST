/*******************************************************************************
* Function Name  : LCD_Write_EnStr
* Description    : Write an English string
* Input          : X, Y, *s
* Output         : None
* Return         : None
*******************************************************************************/
void LCD_Write_EnStr(u8 X, u8 Y, u8 *s)
{
    u8 len = 0;
    u8 *str = s;
    int i = 0;
    int lcd_index =0;
    
    if(str == 0 )
    {
        return ;
    }
	//wait for last DMA request complete
    while(DMA_GetCurrDataCounter(DMA1_Channel5)) ;
        
    LCD_SetXY(X,Y);
    
    while(*str)
    {
		//copy display data to buffer
        for( i = 0; i < 6; i++ )
        {
            lcd_buf[ lcd_index++ ] = Font6x8[ *str - 32 ][ i ];
        }
        str ++ ;
    }
    lcd_buf[ lcd_index++ ] = 0 ; //send one more 0, or the last character would lost a pixel
    
    
    #if 1
    DMA_InitStructure.DMA_BufferSize = lcd_index;
   
    DMA_Cmd(DMA1_Channel5, DISABLE); 
    DMA_Init( DMA1_Channel5, &DMA_InitStructure);
    DMA_Cmd(DMA1_Channel5, ENABLE);

    LCD_DC_DATA();

    SPI_I2S_DMACmd( SPI2, SPI_I2S_DMAReq_Tx, ENABLE);
				
    #else

    //LCD_DC_DATA();
    
    for(i = 0 ;i < lcd_index ;i++)
    {
        LCD_Send(lcd_buf[i], DC_DATA);
    }
    
    #endif
}

