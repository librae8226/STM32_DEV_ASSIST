/*******************************************************************************
* Function Name  : LCD_SetContrast
* Description    : Set LCD contrast : 0 ~ 127 (best within 55 ~ 70)
* Input          : contrast
* Output         : None
* Return         : None
*******************************************************************************/
void LCD_SetContrast(u8 contrast)
{
    LCD_Send(0x21, DC_CMD);
	Wait_ms(10);
    LCD_Send(0x80 | contrast, DC_CMD);
	Wait_ms(10);
    LCD_Send(0x20, DC_CMD);
	Wait_ms(10);
}

/*******************************************************************************
* Function Name  : LCD_SetXY
* Description    : Set current position of LCD cursor
* Input          : X, Y
* Output         : None
* Return         : None
*******************************************************************************/
void LCD_SetXY(u8 X, u8 Y)
{
	if( X > 13 )
	{
		X = 13;
	}
	if( Y > 5 )
	{
		Y = 5;
	}
	X *= 6;
	LCD_Send(0x80 | X, DC_CMD);	//column
	Wait_ms(10);
	LCD_Send(0x40 | Y, DC_CMD);	//row
}

