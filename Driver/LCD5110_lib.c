/*******************************************************************************
* Function Name  : LCD_draw_bmp_pixel
* Description    : bitmap draw function
* Input          : X, Y:start drawing coordinate; 
				   *map:pixel data; 
				   Pix_x, Pix_y:bitmap horizonal length, vertical length
* Output         : None
* Return         : None
*******************************************************************************/
void LCD_draw_bmp_pixel(u8 X, u8 Y,u8 *map, u8 Pix_x,u8 Pix_y)
{
	unsigned int i,n;
	unsigned char row;
	
	if ( Pix_y % 8 == 0 )
		row = Pix_y/8;	//calculate how many rows this bitmap reachs
	else
		row = Pix_y/8 + 1;
	
	for ( n = 0; n < row; n++ )
	{
		LCD_SetXY(X, Y);
		for( i = 0; i < Pix_x; i++ )
		{
			LCD_Send(map[i+n*Pix_x], DC_DATA);
		}
		Y++;	//jump to next row
	}      
}			   

/*******************************************************************************
* Function Name  : LCD_Init
* Description    : LCD initialization
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void LCD_Init(void)
{
	//reset LCD, low-level
	LCD_RST_L();		
	Wait_ms(1);
	LCD_RST_H();
	
	//复位串行接口_高电平脉冲
	//reset serial interface, high-level
	LCD_CE_H(); 
	Wait_ms(1);
	LCD_CE_L(); 

	//config LCD
    LCD_Send(0x21, DC_CMD);	//使用扩展命令设置LCD模式
    LCD_Send(0xC8, DC_CMD);	//设置偏置电压
    LCD_Send(0x06, DC_CMD);	//温度校正
    LCD_Send(0x13, DC_CMD);	//1:48
    LCD_Send(0x20, DC_CMD);	//使用基本命令
    LCD_Send(0x0C, DC_CMD);	//设定显示模式，正常显示

    //LCD_CE_H();

    // for LCD5110
	// initialize DMA
    DMA_InitStructure.DMA_BufferSize = 0 ;
    DMA_InitStructure.DMA_DIR = DMA_DIR_PeripheralDST ;
    DMA_InitStructure.DMA_M2M = DISABLE ;
    DMA_InitStructure.DMA_MemoryBaseAddr = (u32)lcd_buf;
    DMA_InitStructure.DMA_MemoryDataSize = DMA_MemoryDataSize_Byte ;
    DMA_InitStructure.DMA_MemoryInc = DMA_MemoryInc_Enable ;
    DMA_InitStructure.DMA_Mode = DMA_Mode_Normal;
    //spi2 data rigester address 
    DMA_InitStructure.DMA_PeripheralBaseAddr = (u32)&SPI2->DR ;
    DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_Byte ;
    DMA_InitStructure.DMA_PeripheralInc = DMA_PeripheralInc_Disable;
    DMA_InitStructure.DMA_Priority = DMA_Priority_High;
	
	LCD_SetContrast(60); //55~80
	LCD_Clear();
}

