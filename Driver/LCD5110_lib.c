/*******************************************************************************
* Function Name  : LCD_Send
* Description    : Send data or command to LCD
* Input          : data, d/c
* Output         : None
* Return         : None
*******************************************************************************/
void LCD_Send(u8 data, DCType dc)
{
	u8 i=8;

	#if USESPI

    while( SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE ) == RESET );


	if (dc==DC_CMD)
		LCD_DC_CMD();	//send command
	else
		LCD_DC_DATA();	//send data
    
    SPI_I2S_SendData(SPI2, data);

	//wait for sending complete, or LCD_SET_XY would meet some problem
    while( SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE ) == RESET );

	#else

   	if (dc==DC_CMD)
		LCD_DC_CMD();
	else
		LCD_DC_DATA();

	while (i--)
	{
		LCD_CLK_L();
		if(data&0x80)
			LCD_MOSI_H();	//MSB first
		else 
			LCD_MOSI_L();
		LCD_CLK_H();
		data <<= 1;	//Data shift
	}
	#endif	
}

